const FloatingIcons = () => {
    const [floatingIcons, setFloatingIcons] = useState([]);
    const maxIcons = 15;

    const createFloatingIcon = () => {
        const icon = icons[Math.floor(Math.random() * icons.length)];
        const position = {
            left: Math.random() * 100,
            top: Math.random() * 100,
            animationDuration: 2  + Math.random() * 4
        };
        return {
            id: Date.now() + Math.random(),
            icon,
            position
        };
    };

    useEffect(() => {
        const interval = setInterval(() => {
            setFloatingIcons(prev => {
                if (prev.length >= maxIcons) {
                    return [...prev.slice(1), createFloatingIcon()];
                }
                return [...prev, createFloatingIcon()];
            });
        }, 1000);

        return () => clearInterval(interval);
    }, []);

    return (
        <div className="floating-icons-container">
            {floatingIcons.map(({ id, icon, position }) => (
                <div
                    key={id}
                    className="floating-icon"
                    style={{
                        left: `${position.left}%`,
                        top: `${position.top}%`,
                        animationDuration: `${position.animationDuration}s`
                    }}
                >
                    {icon.svg}
                </div>
            ))}
        </div>
    );
};

